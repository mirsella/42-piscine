#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*tab;
	int	i;

	if (min >= max)
		return (NULL);
	tab = malloc(sizeof(int) * (max - min));
	i = min;
	while (i < max)
	{
		tab[i] = min + i;
		i++;
	}
	return (tab);
}

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	actualsize;

	i = 0;
	actualsize = 0;
	while (src[actualsize] != '\0')
		actualsize++;
	if (size == 0)
		return (actualsize);
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (actualsize);
}
